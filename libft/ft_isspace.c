#include "libft.h"

int		ft_isspace(int c)
{
	int		a;

	a = 0;
	a += (c == ' ' || c == '\f' ? 1 : 0);
	a += (c == '\n' || c == '\r' ? 1 : 0);
	a += (c == '\t' || c == '\v' ? 1 : 0);
	return (a);
}
